'''
Take two numbers as input from the user and print their sum, diff, product, and quotient.
'''

x = int(input("First number: "))
y = int(input("Second number: "))

# calculating sum, difference, product, and quotient
print("sum =", x+y)
print("diff =", x-y)
print("product =", x*y)
print("quotient  =", x/y)